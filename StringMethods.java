class StringMethods{
		public static void main(String[] args){
		 String s1="Hydera";
		 String s2="bad";
		 
		 //CONCATENATING OF STRING
		 String s3=s1.concat(s2);
		 System.out.println(s3);
		
		
		//UPPERCASE
		 String s4=s3.toUpperCase();
		 System.out.println(s4);
		 
		 //REPLACE
		 String s5=s4.replace('A','Z');
		 System.out.println(s5);
		 
		 
		


		}

}














