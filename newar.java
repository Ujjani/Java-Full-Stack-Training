class newar{
	public static void main(String[] args)
	{
		
		int d[][]=new int[3][4];
		System.out.println("2D ARRAY");
		for(int i=0; i<3; i++)
		{
			for(int j=0; j<4; j++){
			System.out.print(d[i][j]+"\t");
		
		}
		System.out.println("\n");
		}
		
		System.out.println("ARRAY");
		
		int a[]=new int[5];
		a[2]=55;
		for(int j=0; j<5; j++){
		System.out.println(a[j]);}
		
	}
}